"""
Calculator functions
"""

def add(x: int, y: int) -> int:
    """Add x and y and return result.

    :param x: first number
    :type x: int
    :param y: second number
    :type y: int
    :return: sum of two numbers
    :rtype: int
    """
    return x + y

def subtract(x: int, y: int) -> int:
    """Subtract to numbers"""
    return abs(x - y)